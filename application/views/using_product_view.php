<h4>A Simple Yet Important Addition</h4>
<div class="grid_14 alpha">
<p>
	The DuPont™ StormRoom™ with KEVLAR®, an aboveground, in-home hurricane and tornado shelter, 
	can be easily added to an existing home or incorporated into any new building design. It can 
	be created in any wall length from 4 feet to 12 feet as a customized system or standard size. 
	The homeowner or builder can make StormRoom™ into a fully functional living space; all you need 
	is wiring, plumbing (if desired) and a few finishing touches to create a StormRoom™ that doubles 
	as an extra closet, powder room or laundry room. When installed in a home, DuPont™ StormRoom™ 
	meets FEMA requirements for passive ventilation.
</p>
<p>	
	The DuPont™ StormRoom™ with KEVLAR® is easy to install and offers many benefits:
</p>
<ul>
	<li>Durable and strong yet lighter than steel and concrete shelters</li>
	<li>Can be easily added to an existing home or new construction</li>
	<li>Doesn’t interrupt the building cycle—can be installed from bare slab through framing or drywall</li>
	<li>When installed in a home, DuPont™ StormRoom™ meets FEMA requirements for passive ventilation.</li>
	<li>Handicap accessible door.</li>
	<li>It’s thoroughly storm-tested walls with DuPont™ Kevlar® allow for wireless use and radio reception, 
		so you won’t be cut off from the outside.</li>
</ul>
